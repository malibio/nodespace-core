//! MCP Server Integration
//!
//! This module provides the Model Context Protocol (MCP) stdio server implementation
//! that allows AI agents to interact with NodeSpace programmatically:
//!
//! - JSON-RPC communication over stdio
//! - Request routing to core services
//! - Event notification for reactive updates
//!
//! The MCP server enables AI assistants like Claude to read and manipulate
//! nodes, search content, and execute operations within the knowledge base.
